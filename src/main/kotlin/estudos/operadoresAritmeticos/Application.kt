package estudos.operadoresAritmeticos

fun main() {
    var aux = 10
    var aux1 = 2

    //SOMA
    var soma = aux + aux1
    //aux.plus(aux1)
    println(soma)

    //SUBTRACAO
    var subtracao = aux - aux1
    //aux.minus(aux1)
    println(subtracao)

    //MULTIPLICACAO
    var multiplicacao = aux * aux1
    //aux.times(aux1)
    println(multiplicacao)

    //DIVISAO
    var divisao = aux / aux1
    //aux.div(aux1)
    println(divisao)

    //RESTO
    var resto = aux % aux1
    //aux.mod(aux1)
    println(resto)
}